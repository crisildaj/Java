package com.mycompany.theprimitiveclasses;

public class ThePrimitiveClasses {

    public static void main(String[] args) {
        //String = reference data type (class)
        String s = "string";

        //char = primitive data type
        char c = 'C';

        // Check if char c is lower case
        System.out.println(Character.isLowerCase(c));
    }
}
